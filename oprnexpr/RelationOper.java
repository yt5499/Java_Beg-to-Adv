package oprnexpr;

//	Relational operator
public class RelationOper {

	public static void main(String[] args) {
		
		int a = 25, b = 30;
		
//		display
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		System.out.println();
		
//		operators
		System.out.println("a>b: "+(a>b));
		System.out.println("a<b: "+(a<b));
		System.out.println("a>=b: "+(a>=b));
		System.out.println("a<=b: "+(a<=b));
		System.out.println("a==b: "+(a==b));
		System.out.println("a!=b: "+(a!=b));

	}

}
