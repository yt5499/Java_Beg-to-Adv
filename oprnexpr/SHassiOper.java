package oprnexpr;

//using short-hand assignment operators
public class SHassiOper {

	public static void main(String[] args) {
		
		int a = 5, b = 10, c = 15;
		
//		short-hand assignment operators
		a += 5;
		b -=5;
		c *=2;
		c %=10;
		
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		System.out.println("c= "+c);
	}

}
