package selection;

public class SwitchCase {

	public static void main(String[] args) {
		
		String a = "hi";
		
		switch (a) {
		case "hello": 
			System.out.println("Its hello");
			break;
		case "hi": 
			System.out.println("Its hi");
			break;
		default:
			System.out.println("Invalid");
		}

	}

}
